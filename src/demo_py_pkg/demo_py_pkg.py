def add_numbers(x, y):
    """Add two numbers together.
    
   Parameters
   ----------
   x : int
       First number
   y : int
       Second number
   
   Returns
   -------
   int
       The sum of x and y

    Examples
    --------
    >>> add_numbers(1, 2)
    3 
    """
    return x + y

def subtract_numbers(x, y):
    """Subtract two numbers.
    
    Parameters
    ----------
    x : int
        First number
    y : int
        Second number
    
    Returns
    -------
    int
        The difference of x and y
    
    Examples
    --------
    >>> subtract_numbers(1, 2)
    -1
    """
    return x - y